<h2 class="charte">Présentation de l'Ecole Nationales des Sciences Appliquées de Tétouan :</h2>
<article  class="charte">
  <p>&nbsp;</p>
<p>Créée en Septembre 2008, L’ENSA- Tétouan membre du réseau des Ecoles Nationales des Sciences Appliquées, est un établissement public à caractère scientifique culturel et professionnel, instauré pour être une école d’ingénieurs de haut niveau.<br>
</p>
<p>
L’école a pour vocation principale de former des ingénieurs d’état rapidement opérationnel, particulièrement adaptable aux évolutions de la technologie et aux mutations de la société. Elle offre à ses étudiants une insertion professionnelle, à travers une pédagogie de l’autonomie et une adaptation technologique transdisciplinaire orientée vers l’innovation.
</p>
<p>L’école offre des formations dans les filières et options suivantes :</p><br>
 <ul class="filiere">
 <li class="">
 Génie des Systèmes des Télécommunications et Réseaux.
 </li>
  <li class="">
 Génie Informatique.
 </li>
 <li class="">
 Ingénierie Logistique Et Transport. </li>
 <li class="">
 Ingénieur en Mécatronique.
 </li>
 <li class="">
 Génie civil.
 </li>
 </ul>
 <br>
 <p>L’école offre des formations dans les filières et options suivantes :</p>
 <p>En voie de maturité l’ENSA- Tétouan a pour objectif de se positionner parmi les meilleurs établissements d’enseignement supérieur et d’augmenter sa notoriété dans l’environnement socio-économique.</p>
 <p>La structure pédagogique de l’ENSA est organisée en deux cycles : un Cycle Préparatoire intégré de 2 ans et un Cycle Ingénieur de 3 ans.</p>
 <p>
 La formation à l’ENSA-Tétouan s’inscrit dans le cadre de l’amélioration du développement des entreprises et l’amélioration de la qualité de leurs ressources humaines.
 </p>
 <p>
 Elle vise à mettre à la disposition des entreprises des cadres et des managers polyvalents dans leurs domaines de compétences, capables de prendre en charge des postes de responsabilité de haut niveau.
La diversité des filières offertes à l’ENSA -Tétouan conduit à couvrir un ensemble d’activités professionnelles touchant différents secteurs et domaines économiques.
 </p>
 <p>
 Le cycle préparatoire
 </p>
 <p>
 Couvrant la première et la deuxième année du cursus (soit 5 ans), le cycle préparatoire permet à l’étudiant de consolider sa formation dans les sciences de base et de s’initier aux techniques de communication, à l’outil informatique, à la technologie, aux sciences humaines et à la gestion d’entreprise.
 </p>
 <p>L’enseignement se base sur des cours, des travaux dirigés et des travaux pratiques.</p><br>
  <ul class="annee">
  <li class="">
La 1ère année est une année de travail intense.
 </li><br>
 <li class="">
La 2ème année est une année d’orientation préparant les étudiants au cycle ingénieur
 </li>
 </ul><br>
 <p>Le Cycle Ingénieur</p>
 <p>Le Cycle Ingénieur est étalé sur 3 ans (soit 6 semestres). une filière Ingénieur est un cursus de formation comprenant un ensemble cohérent de modules pris dans un ou plusieurs champs disciplinaires d’ingénieurs ou de disciplines connexes et ayant pour objectif de faire acquérir à l’étudiant des connaissances, des aptitudes et des compétences spécifiques.</p>
 <p>Les cinq premiers semestres sont composés de trois blocs de modules :</p><br>
 <p>
  <ul class="block">
  <li class="">
Bloc des modules scientifique, techniques de base et de spécialisation.
 </li>
 <li class="">
 Bloc de modules de management.
 </li>
 <li class="">
Bloc de modules de langues, de communication et des TEC.
 </li>
 </ul>
 </p><br>
 <p>Le sixième semestre est consacré au PFE.</p></article>
